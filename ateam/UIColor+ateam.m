#import "UIColor+ateam.h"

@implementation UIColor (ateam)

+ (UIColor *)ateamDarkestRed
{
    return [UIColor colorWithRed:0.17 green:0.01 blue:0.07 alpha:1.0];
}

+ (UIColor *)ateamDarkRed
{
    return [UIColor colorWithRed:0.24 green:0.05 blue:0.10 alpha:1.0];
}

+ (UIColor *)ateamWhite
{
    return [UIColor colorWithRed:0.98 green:1.00 blue:0.98 alpha:1.0];
}

+ (UIColor *)ateamBlue
{
    return [UIColor colorWithRed:0.27 green:0.61 blue:0.67 alpha:1.0];
}

+ (UIColor *)ateamRed
{
    return [UIColor colorWithRed:0.91 green:0.10 blue:0.15 alpha:1.0];
}

@end
